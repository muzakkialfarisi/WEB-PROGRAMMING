                        <div class="aFAQ pl-3">
                            Untuk mengetahui NISN,  silakan cek di menu DATA SISWA atau klik pada <a href="<?= base_url() ?>HomeNISN/index4">link</a> ini pada situs NISN, dengan cara:
                            <ul>
                                <li><img src="<?= base_url() ?>images/index4.1.png" border="0" alt="Perncarian Berdasakan NISN"></li>
                                <br>
                                <br>
                                <li><img src="<?= base_url() ?>images/index4.1.png" border="0" alt="Perncarian Berdasakan Nama"></li>
                                <br>
                                Berdasarkan kedua kriteria diatas, maka akan muncul siswa  aktif dengan NISN-nya. Jika tidak ada sama sekali berarti sekolah belum  mendaftarkan siswanya.
                            </ul>
                        </div>
                    </div> <br>
                    <div class="qFAQ">
                        <b>Saya belum memiliki NISN, bagaimana  cara memperolehnya?</b>
                        <div class="aFAQ pl-3">
                            <ul>
                                <li>Dianjurkan terlebih dahulu untuk melaksanakan solusi seperti pada permasalahan sebelumnya (diatas)</li>
                                <li>Jika siswa tidak ditemukan, silakan download (unduh) Formulir  Peserta Didik (F-PD) dan formulir A.1  pengajuan Nomor Induk Siswa Nasional (NISN) baru pada situs NISN  http://nisn.data.kemdiknas.go.id melalui  menu FORMULIR PENGAJUAN;</li>
                                <li>Mencetak formulir-formulir tersebut;</li>
                                <li>Mengisi formulir-formulir tersebut secara manual;</li>
                                <li>Menyerahkan kepada operator NISN/NPSN sekolah dimana siswa  berasal;</li>
                                <li>Bagi pihak sekolah dapat mengirim formulir A.1. tersebut ke  PDSP melalui email : pdsp@kemdiknas.go.id untuk diberikan NISN.</li>
                            </ul>
                        </div>
                    </div><br>
                    <div class="qFAQ">
                        <b>Bagaimana caranya memperbaiki  biodata NISN? Karena yang tertera tidak sesuai dengan akta kelahiran?</b></h3>
                        <div class="aFAQ pl-3">
                            <ul>
                                <li>Silakan download (unduh) formulir A.3. Pengajuan Perbaikan  Biodata Siswa di situs NISN http://nisn.data.kemdiknas.go.id&nbsp; pada menu FORMULIR PENGAJUAN;</li>
                                <li>Mencetak formulir tersebut;</li>
                                <li>Mengisi formulir tersebut secara manual;</li>
                                <li>Menyerahkan kepada operator tingkat sekolah dimana NISN  tersebut di terbitkan;</li>
                                <li>Bagi sekolah, dapat mengirimkan formulir yang sudah  diperbaiki tersebut ke&nbsp; PDSP melalui  email : pdsp@kemdiknas.go.id.</li>
                            </ul>
                        </div>
                    </div><br>
                    <div class="qFAQ">
                        <b>Di sekolah saya yang sekarang  ditanyakan NISN. Padahal pada saat lulus dari sekolah sebelumnya tidak dibekali NISN?</b>
                        <div class="aFAQ pl-3">
                            Dianjurkan terlebih dahulu  untuk melaksanakan solusi seperti pada permasalahan No.1 diatas;
                            <ul>
                                <li>Jika tidak ditemukan, silakan download (unduh) formulir  peserta didik (F-PD) dan formulir A.1. pengajuan Nomor Induk Siswa Nasional  (NISN) baru pada situs NISN http://nisn.data.kemdiknas.go.id&nbsp; melalui menu FORMULIR PENGAJUAN;</li>
                                <li>Mencetak formulir-formulir tersebut;</li>
                                <li>Mengisi formulir-formulir tersebut secara manual; </li>
                                <li>Menyerahkan kepada operator NISN/NPSN sekolah asal.</li>
                                <li>Bagi pihak sekolah dapat mengirim formulir A.1. tersebut ke  PDSP melalui email : pdsp@kemdiknas.go.id untuk diberikan NISN.</li>
                            </ul>
                        </div>
                    </div><br>
                    <div class="qFAQ">
                        <b>Kartu NISN saya hilang, datanya juga  ada yang keliru, bagaimana ini?</b>
                        <div class="aFAQ pl-3">
                            Jika  terjadi kesalahan, kerusakan atau kehilangan kartu NISN, silakan dilaporkan ke  operator NISN/NPSN sekolah dimana NISN tersebut diterbitkan.<br>
                        </div>
                    </div><br>
                    <div class="qFAQ">
                        <b>Bagaimana jika ada 1 orang siswa memiliki 2  (dua) NISN? NISN yang mana yang di digunakan.</b>
                        <div class="aFAQ pl-3">
                            NISN yang digunakan  adalah NISN yang sudah digunakan sebagai alat administrasi dan validasi oleh siswa di sekolahnya. Jika tidak, NISN yang digunakan adalah NISN yang paling  kecil kodenya. Untuk NISN yang tidak digunakan mohon segera melapor ke operator  NISN/NPSN sekolah asal untuk segera dihapus.
                        </div>
