            <b>Nomor  Induk Siswa Nasional (NISN)&nbsp;</b>
        </div><br>
        <div style="margin-left: 20px; font-size: 1.2em;">
            1. Pengertian
        </div>
        <div style="margin-left: 40px">
            Nomor  Induk Siswa Nasional adalah, kode pengenal identitas siswa yang bersifat unik,  standar dan berlaku sepanjang masa yang dapat membedakan satu siswa dengan  siswa lainnya di seluruh sekolah Indonesia dan Sekolah Indonesia di Luar  Negeri; Nomor Induk Siswa Nasional (NISN) diberikan kepada setiap peserta didik  yang bersekolah di satuan pendidikan yang memiliki NPSN dan terdaftar di  Referensi Kemendikbud. <br>
            Sistem  pengelolaan NISN secara nasional oleh Pusat Data dan Statistik Pendidikan dan  Kebudayaan (PDSPK) Kemendikbud yang merupakan bagian dari program Dapodik (Data  Pokok Pendidikan) Kementerian Pendidikan dan Kebudayaan. Hasil dari proses  pemberian kode identifikasi oleh PDSPK ditampilkan secara terbuka dalam batasan  tertentu melalui situs NISN (<em>http://nisn.data.kemdikbud.go.id./</em>).
        </div><br>
        
        <div style="margin-left: 20px; font-size: 1.2em;">
            2. Tujuan dan Manfaat
        </div>
